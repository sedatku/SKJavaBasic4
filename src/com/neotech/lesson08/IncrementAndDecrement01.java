package com.neotech.lesson08;

public class IncrementAndDecrement01 {

	public static void main(String[] args) {
		int x = 10;
		
		//reassigning
		//x = 11;
		x = x + 1;
		
		//shorthand op
		
		x+=1;
		
		//increment
		x++;
		
		System.out.println(x);
		
		
		int y = 50;
		
		y = y-1;
		
		//shorthand op
		y -=1;
		
		//decrement
		y--;
		
		System.out.println(y);
		

	}

}
