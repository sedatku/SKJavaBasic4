package com.neotech.lesson02;

public class Variables {
	public static void main(String[] args) {
		int variable1 = 15;
		int someNumber = 5;

		System.out.println(20);

		// if you want to print the value that is inside
		// the container with name variable
		System.out.println(variable1);
		System.out.println(someNumber);

		// This will print the text/word someNumber
		// Not the value that is stored in the container
		System.out.println("someNumber");

		// Compiler is giving an error, saying "VariableOne cannot be resolved"
		// System.out.println(variableOne);

	}

}
