package com.neotech.lesson03;

public class OperationsOrder {

	public static void main(String[] args) {
		// PEMDAS - Parenthesis, Exp, Mult, Div, Add, Sub

		int num1, num2, num3;

		num1 = 10 + 10 * 2;
		System.out.println(num1);
		
		num2 = (10 + 10) * 2;
		System.out.println(num2);
		
		num3 = 5 * 5 + 25/5  - 5; 
		System.out.println(num3);

	}

}
